void main()
{
  int num1 = 10;
  int num2 = 5;

  print(num1 + num2);
  print(num1 - num2);
  print(num1 * num2);
  print(num1 / num2 );
  //interger division
  print(num1 ~/ num2);
  //unary minus (negation)
  print(-num1);
  //remain division
  print(num1 % num2);
  //increment
  num1++;
  print(num1);
  //Decrement
  num2--;
  print(num2);
}
